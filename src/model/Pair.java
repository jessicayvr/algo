package model;

public class Pair {
    private int x;
    private int y;
    private int val;

    public Pair(int x, int y, int val) {
        this.x = x;
        this.y = y;
        this.val = val;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }

    public int getVal() {
        return this.val;
    }
}
